    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String mensaje = "Las ciudades del Pais seleccionado son:\n";
        List<String> nCiudades = new ArrayList();
        Country p = null;
        String nombre = cmbPaises.getSelectedItem().toString();
        for (int i = 0; i < paises.size(); i++) {
            if (nombre.compareToIgnoreCase(paises.get(i).getCountry()) == 0) {
                p = paises.get(i);
            }
        }
        nCiudades = Consultas.CiudadesDeUnPais(p);
        for (int x = 0; x < nCiudades.size(); x++) {
            mensaje += nCiudades.get(x) + "\n";
        }
        txtArea.setText(mensaje);
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String mensaje = "Los Paises que empiezan por la letra dada son:\n";
        List<String> nPaises = new ArrayList();
        String nombre = jtInicial.getText();
        nPaises = Consultas.PaisesQueEmpiezan(nombre);
        if (nPaises.isEmpty()) {
            String mensaje2 = "Ningun pais comienza por esa letra";
            txtArea.setText(mensaje2);
        } else {
            for (int x = 0; x < nPaises.size(); x++) {
                mensaje += nPaises.get(x) + "\n";
            }
            txtArea.setText(mensaje);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

