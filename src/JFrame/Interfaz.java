    public void inicializarPaises() {
        paises = new ArrayList();
        SessionFactory conexion = NewHibernateUtil.crearSesion();
        Session sesion = conexion.openSession();
        org.hibernate.Transaction tr = sesion.beginTransaction();
        Query consulta = sesion.createNativeQuery("SELECT * FROM Country", Country.class);
        paises = (ArrayList<Country>) consulta.getResultList();
        tr.commit();
        sesion.close();
        conexion.close();
    }

    public void cargaComboDePaieses() {
        int i;
        String puestoCombo;
        for (i = 0; i < paises.size(); i++) {
            puestoCombo = paises.get(i).getCountry();
            cmbPaises.addItem(puestoCombo);
        }
    }

